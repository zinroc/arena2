module.exports = function (knex) {
    return {
        read: function () {
            return knex("game_state").select("*").where("id", 1)
            .then(function (rows) {
                return rows[0];
            });
        },
        update: function (gameState) {
            return knex("game_state").where("id", 1)
            .update(gameState);
        }
    };
};
